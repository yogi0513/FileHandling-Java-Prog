package NaveenRealTimeJava;

/*
 *  STar pattern -- 
 */
public class P23_StarPatterns {
	
	public static void main(String[] args) {
		
		char in ='A';
		for(int i=1;i<=6;i++) {
			for(int j=1;j<=i;j++) {
				System.out.print(in+" ");			
			}
			in++;
			System.out.println();
		}
		
		
		
	}
}

