package NaveenRealTimeJava;

public class P07NumDivByZero {

		/*
		 * What will be the output when you divide a number by zero?
		 */
	
		public static void main(String[] args) {
			
			System.out.println(9.0/0);		//infinity
			System.out.println(12.33f/0);  //infinity
			System.out.println(10/0.0);    //infinity
			System.out.println(0/0);  // arithmetic exception --- comes only with integer
			
			
			
			
		}
}
